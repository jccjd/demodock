import asyncio
import os
from datetime import timedelta

from opensandbox import Sandbox
from opensandbox.config import ConnectionConfig
from opensandbox.models.execd import RunCommandOpts


def _required_env(name: str) -> str:
    value = os.getenv(name)
    if not value:
        raise RuntimeError(f"{name} is required")
    return value


async def _print_execution_logs(execution) -> None:
    for msg in execution.logs.stdout:
        print(f"[stdout] {msg.text}")
    for msg in execution.logs.stderr:
        print(f"[stderr] {msg.text}")
    if execution.error:
        print(f"[error] {execution.error.name}: {execution.error.value}")


async def main() -> None:
    domain = os.getenv("SANDBOX_DOMAIN", "localhost:8080")
    api_key = os.getenv("SANDBOX_API_KEY")
    iflow_api_key = os.getenv("IFLOW_API_KEY", "sk-5674661b611f3ef28ab5d53b594c3cb6")
    iflow_base_url = os.getenv("IFLOW_BASE_URL", "https://apis.iflow.cn/v1")
    iflow_model_name = os.getenv("IFLOW_MODEL_NAME", "qwen3-coder-plus")
    image = os.getenv(
        "SANDBOX_DESKTOP_IFLOW_IMAGE",
        "opensandbox/desktop-iflow:latest",
    )
    python_version = os.getenv("PYTHON_VERSION", "3.11")
    vnc_password = os.getenv("VNC_PASSWORD", "opensandbox")

    config = ConnectionConfig(
        domain=domain,
        api_key=api_key,
        request_timeout=timedelta(seconds=60),
    )

    sandbox = await Sandbox.create(
        image,
        connection_config=config,
        env={
            "PYTHON_VERSION": python_version,
            "VNC_PASSWORD": vnc_password,
            "IFLOW_apiKey": iflow_api_key,
            "IFLOW_baseUrl": iflow_base_url,
            "IFLOW_modelName": iflow_model_name,
        },
    )

    async with sandbox:
        print("Starting desktop environment with pre-installed iFlow CLI...")
        
        # Start virtual display, window manager, and VNC server (in background)
        xvfb_exec = await sandbox.commands.run(
            "Xvfb :0 -screen 0 1280x800x24",
            opts=RunCommandOpts(background=True),
        )
        await _print_execution_logs(xvfb_exec)

        # Start XFCE session (provides panel, file manager, terminal)
        xfce_exec = await sandbox.commands.run(
            "DISPLAY=:0 dbus-launch startxfce4",
            opts=RunCommandOpts(background=True),
        )
        await _print_execution_logs(xfce_exec)

        vnc_exec = await sandbox.commands.run(
            "x11vnc -display :0 "
            "-passwd \"$VNC_PASSWORD\" "
            "-forever -shared -rfbport 5900",
            opts=RunCommandOpts(background=True),
        )
        await _print_execution_logs(vnc_exec)

        # Start noVNC/websockify to expose VNC over WebSocket/HTTP
        novnc_exec = await sandbox.commands.run(
            "/usr/bin/websockify --web=/usr/share/novnc 6080 localhost:5900",
            opts=RunCommandOpts(background=True),
        )
        await _print_execution_logs(novnc_exec)

        endpoint_vnc = await sandbox.get_endpoint(5900)
        endpoint_novnc = await sandbox.get_endpoint(6080)

        # Build noVNC URL with host/port/path for routed endpoint, e.g., host:port/proxy/6080
        novnc_host_port, novnc_path = endpoint_novnc.endpoint.split("/", 1)
        novnc_host, novnc_port = novnc_host_port.split(":")
        novnc_url = (
            f"http://{endpoint_novnc.endpoint}/vnc.html"
            f"?host={novnc_host}&port={novnc_port}&path={novnc_path}"
        )

        print("\nVNC endpoint (native clients):")
        print(f"  {endpoint_vnc.endpoint}")
        print(f"Password: {vnc_password}")

        print("\nnoVNC (browser):")
        print(f"  {novnc_url}")
        print(f"Password: {vnc_password}")

        # Check if iFlow CLI is available
        check_exec = await sandbox.commands.run('which iflow')
        print("\niFlow CLI check:")
        await _print_execution_logs(check_exec)

        # Run a simple iFlow command
        print("\nRunning iFlow CLI command...")
        run_exec = await sandbox.commands.run('iflow "Compute 1+1=?."')
        await _print_execution_logs(run_exec)

        print("\nKeeping sandbox alive for 2 minutes to allow VNC access. Press Ctrl+C to exit sooner.")
        try:
            await asyncio.sleep(120)
        except KeyboardInterrupt:
            print("Stopping...")
        finally:
            await sandbox.kill()


if __name__ == "__main__":
    asyncio.run(main())
